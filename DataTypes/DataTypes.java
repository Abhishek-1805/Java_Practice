package DataTypes;

public class DataTypes 
{
	public static void main(String[] args) 
	{
//		Integer
		byte bt = 127;
		short sh  = 558;
		int in = 55868;
		long lo = 61656884l;
// Decimal
		float fl  = 5.5f;
		double dd = 5352.22;
//character
		char word = 'a';
//boolean
		boolean a = true;
		
		System.out.println(bt + sh + in + lo+ " " +"This are integer");
		System.out.println(fl + dd + " " +"This are decimal");
		System.out.println(word + " " + "Character");
		System.out.println(a + " " +" Boolean");
		
	}

}
